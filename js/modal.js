$('#coach-1').click(function () {
	document.getElementById('coach-picture-modal').src=document.getElementById('coach-picture-1').src;
	document.getElementById('coachModalLabel').innerHTML=document.getElementById('coach-1').innerHTML;
	document.getElementById('major').innerHTML="Computer Engineering";
	document.getElementById('school').innerHTML="University of Virginia";
	document.getElementById('coach-bio').innerHTML="I started off double majoring in computer science and applied math at the College of William & Mary. " +
                                "During my free time, I would volunteer as a tutor for public schools in Williamsburg, VA, and " + 
                                "eventually, I was hired as a tutor for W&M. I then became fascinated with computer engineering, " + 
                                "which led me to transfer to the University of Virginia to study computer engineering. I am now a " +  
                                "senior at UVA and have been privately tutoring for the past couple years with great success. " +
                                "Please feel free to check out some of my <a href='https://www.wyzant.com/Tutors/STEM-Avery' target='_blank'>student reviews</a>.";
});

